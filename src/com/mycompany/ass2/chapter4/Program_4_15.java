public class Program_4_15//Increment Class
{

	/**
	 * @param args
	 */
	public static void main(String[] args)
	{
		int c = 5, d = 5;
		System.out.printf("c: %d%n", c);
		System.out.printf("Postincrementing c: %d%n", c++);
		System.out.printf("New c: %d%n", c);
		System.out.printf("d: %d%n", d);
		System.out.printf("Preincrementing d: %d%n", ++d);
		System.out.printf("New d: %d%n", d);
	}

}
